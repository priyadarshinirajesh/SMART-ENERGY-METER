#define SAMPLES 1000                // Number of samples for RMS calculation
#define ACS712_OFFSET 2048          // Adjust based on calibration (midpoint for 5V ADC)
#define ZMPT101B_OFFSET 2048        // Adjust based on calibration (midpoint for 5V ADC)
#define VREF 3.3                    // Reference voltage for ADC
#define ADC_RES 4096.0              // 12-bit ADC resolution (2^12)
#define ZMPT101B_SENSITIVITY 1  // Sensitivity for ZMPT101B (approx. V per V of input)
#define ACS712_SENSITIVITY 10000    // Sensitivity for ACS712 (0.066 V/A for 30A model)

float current_rms = 0.0;
float voltage_rms = 0.0;
float current_sum_sq = 0.0;
float voltage_sum_sq = 0.0;
char buffer[16];

// Square Root Approximation Function
float sqrt_approx(float num) {
    float approx = num / 2.0;
    for (int i = 0; i < 30; i++) { // Iterates to improve accuracy
        approx = (approx + num / approx) / 2.0;
    }
    return approx;
}

int main(void) {
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_ADC1_Init();
    MX_TIM1_Init();
    HAL_TIM_Base_Start(&htim1);
    lcd_init();
    lcd_put_cur(0,0);
    HAL_Delay(2000);

    while (1) {
        current_sum_sq = 0.0;
        voltage_sum_sq = 0.0;

        // Take samples for RMS calculation
        for (int i = 0; i < SAMPLES; i++) {
            // Configure ADC for ACS712 on PA1 (Channel 1)
            ADC_ChannelConfTypeDef sConfig = {0};
            sConfig.Channel = ADC_CHANNEL_1;
            sConfig.Rank = 1;
            HAL_ADC_ConfigChannel(&hadc1, &sConfig);

            // Start ADC conversion for ACS712 (Current)
            HAL_ADC_Start(&hadc1);
            HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
            uint32_t adc_val_current = HAL_ADC_GetValue(&hadc1);

            // Offset adjustment and scaling for ACS712 (Current in Amps)
            float current = ((float)(adc_val_current - ACS712_OFFSET) * VREF / ADC_RES) / ACS712_SENSITIVITY;

            // Configure ADC for ZMPT101B on PA2 (Channel 2)
            sConfig.Channel = ADC_CHANNEL_2;
            HAL_ADC_ConfigChannel(&hadc1, &sConfig);

            // Start ADC conversion for ZMPT101B (Voltage)
            HAL_ADC_Start(&hadc1);
            HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
            uint32_t adc_val_voltage = HAL_ADC_GetValue(&hadc1);

            // Offset adjustment and scaling for ZMPT101B (Voltage in Volts)
            float voltage = ((float)(adc_val_voltage - ZMPT101B_OFFSET) * VREF / ADC_RES) / ZMPT101B_SENSITIVITY;

            // Summing the squares for RMS calculation
            current_sum_sq += current * current;
            voltage_sum_sq += voltage * voltage;
        }

        // RMS calculations using square root approximation
        current_rms = sqrt_approx(current_sum_sq / SAMPLES);
        voltage_rms = sqrt_approx(voltage_sum_sq / SAMPLES);
        current_rms = current_rms/100;
        voltage_rms = voltage_rms * 260;
        // Display on LCD
        lcd_clear();
        lcd_put_cur(0, 0);
        sprintf(buffer, "I: %.2f A", current_rms);
        lcd_send_string(buffer);

        lcd_put_cur(1, 0);
        sprintf(buffer, "V: %.2f V", voltage_rms);
        lcd_send_string(buffer);

        HAL_Delay(1000);
    }
}

